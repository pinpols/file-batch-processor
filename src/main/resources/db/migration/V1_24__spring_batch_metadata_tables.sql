-- Spring Batch JDBC metadata tables.
-- Without these tables Spring Batch falls back to ResourcelessJobRepository,
-- which only keeps one JobInstance in memory and causes cross-task collisions.

CREATE TABLE IF NOT EXISTS BATCH_JOB_INSTANCE (
    JOB_INSTANCE_ID BIGINT NOT NULL PRIMARY KEY,
    VERSION BIGINT,
    JOB_NAME VARCHAR(100) NOT NULL,
    JOB_KEY VARCHAR(32) NOT NULL
);

CREATE UNIQUE INDEX IF NOT EXISTS JOB_INST_UN
    ON BATCH_JOB_INSTANCE (JOB_NAME, JOB_KEY);

CREATE TABLE IF NOT EXISTS BATCH_JOB_EXECUTION (
    JOB_EXECUTION_ID BIGINT NOT NULL PRIMARY KEY,
    VERSION BIGINT,
    JOB_INSTANCE_ID BIGINT NOT NULL,
    CREATE_TIME TIMESTAMP NOT NULL,
    START_TIME TIMESTAMP DEFAULT NULL,
    END_TIME TIMESTAMP DEFAULT NULL,
    STATUS VARCHAR(10),
    EXIT_CODE VARCHAR(2500),
    EXIT_MESSAGE VARCHAR(2500),
    LAST_UPDATED TIMESTAMP
);

DO $$
BEGIN
    IF NOT EXISTS (
        SELECT 1
        FROM pg_constraint
        WHERE conname = 'JOB_INST_EXEC_FK'
    ) THEN
        ALTER TABLE BATCH_JOB_EXECUTION
            ADD CONSTRAINT JOB_INST_EXEC_FK
            FOREIGN KEY (JOB_INSTANCE_ID)
            REFERENCES BATCH_JOB_INSTANCE(JOB_INSTANCE_ID);
    END IF;
END $$;

CREATE TABLE IF NOT EXISTS BATCH_JOB_EXECUTION_PARAMS (
    JOB_EXECUTION_ID BIGINT NOT NULL,
